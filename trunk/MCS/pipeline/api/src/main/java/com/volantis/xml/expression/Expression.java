package com.volantis.xml.expression;

/**
 * A parsed expression.
 *
 * <p><strong>Warning: This is a facade provided for use by user code, not for
 * implementation in user code. User implementations of this interface are
 * highly likely to be incompatible with future releases of the product at both
 * binary and source levels.</strong></p>
 *
 * <p><strong>Implementations of this interface must be thread safe and must
 * not maintain any evaluation specific state as they may be evaluated
 * simultaneously by multiple threads.</strong></p>
 *
 * <p>Expressions must have no context specific information in them. Where it
 * is necessary to refer to named items the names must be stored as QNames
 * which are expanded at evaluation time. This means that a given string will
 * always result in the same Expression expression no matter where it is
 * used.</p>
 *
 * @volantis-api-include-in PublicAPI
 * @volantis-api-include-in ProfessionalServicesAPI
 * @volantis-api-include-in InternalAPI
 */
public interface Expression {
    
    /**
     * Evaluate the expression.
     *
     * @param context The context within which the expression is evaluated.
     * @return The result of the expression.
     * @throws ExpressionException An exception if the exception resulted in an
     *                             error.
     */
    public Value evaluate(ExpressionContext context)
            throws ExpressionException;
}

