import com.volantis.xml.expression.functions.AbstractFunction;

/**
 *  This is the superclass of all expression handling functions.
 */
public abstract class AbstractExpressionFunction extends AbstractFunction {

    /**
     * Provide the DefaultValueProvider to use should the default value
     * for the AbstractExpressionFunction be required.
     *
     * @return The DefaultValueProvider for the function.
     */
    protected abstract DefaultValueProvider getDefaultValueProvider();     
}

