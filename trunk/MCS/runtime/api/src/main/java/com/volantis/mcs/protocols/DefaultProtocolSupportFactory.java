package com.volantis.mcs.protocols;

import com.volantis.mcs.dom.DOMFactory;

/**
 * The default implementation of {@link ProtocolSupportFactory}.
 */
public class DefaultProtocolSupportFactory
        implements ProtocolSupportFactory {

    /**
     * The factory to use to create DOM objects within the protocol.
     */
    private static final DOMFactory DOM_FACTORY =
            DOMFactory.getDefaultInstance();

    // Javadoc inherited.
    public DOMFactory getDOMFactory() {
        return DOM_FACTORY;
    }
}

